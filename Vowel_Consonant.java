public class Vowel_Consonant {
    public static void main(String[] args)
    {
        char c='a';
        if(c=='a'||c=='e'||c=='i'||c=='o'||c=='u'||c=='A'||c=='E'||c=='I'||c=='O'||c=='U')
            System.out.println("The character "+c+" is a Vowel");
        else
            System.out.println("The character is a consonant");
    }
}